#include "TcpClient.h"

/*****************************************************************************/
TcpClient::TcpClient ()
{

}
/*****************************************************************************/
bool TcpClient::Start()
{
    /*************************************************************/
    /* Create an AF_INET stream socket                           */
    /*************************************************************/
    if (!Create())
    {
        return false;
    }

    if (!SetBlocking(true))
    {
        return false;
    }

    return true;
}



